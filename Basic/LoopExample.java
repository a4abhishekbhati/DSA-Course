
public class LoopExample {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("Count: " + i);
            i++;
        }
        System.out.println("While loop Ended :- " +i);

        int j;
        for (j = 1; j <= 5; j++) {
            System.out.println("Count: " + j);
        }
        System.out.println("For loop Ended :- " +j);
    }
    
}
